<script>
    function validateForm() {
        var customerName = document.getElementById('nameCustomer').value;
        if (customerName.trim() === '') {
            alert('Please enter your name.');
            return false;
        }
        updateCartUI();
        return true;
    }


    function updateCartUI() {
        var cartItemList = document.getElementById('cartItemList');
        cartItemList.innerHTML = '';
        var itemCountElement = document.getElementById('cartItemCount');
        var totalQuantityElement = document.getElementById('totalQuantity');
        var cartTotalElement = document.getElementById('cartTotal');
        var totalPrice = 0;
        var totalQuantity = 0;

        cartItems.forEach(function(item, index) {
            var listItem = document.createElement('li');
            listItem.className = 'list-group-item d-flex justify-content-between lh-sm';
            listItem.innerHTML = `
        <div>
            <h6 class="my-0">${item.name}</h6>
            <small class="text-muted">Quantity: 
                <button class="btn btn-sm btn-primary mx-1" onclick="decreaseQuantity(${index})">-</button>
                <span id="quantity_${index}">${item.quantity}</span>
                <button class="btn btn-sm btn-primary mx-1" onclick="increaseQuantity(${index})">+</button>
            </small>
        </div>
        <div>
            <span class="text-muted">${item.total}</span>
            <button class="btn btn-sm btn-danger" onclick="removeItem(${item.id})" data-item-id="${item.id}">Remove</button>
        </div>
    `;
            cartItemList.appendChild(listItem);
            totalPrice += item.total;
            totalQuantity += item.quantity;
        });

        itemCountElement.innerText = cartItems.length;
        totalQuantityElement.innerText = totalQuantity;
        var formatter = new Intl.NumberFormat('id-ID', {
            style: 'currency',
            currency: 'IDR'
        });
        cartTotalElement.innerText = formatter.format(totalPrice);

        var jumlahBayar = parseFloat(document.getElementById('jumlahBayar').value);
        var status = jumlahBayar >= totalPrice ? 'lunas' : 'belum lunas';
        var kembalian = jumlahBayar - totalPrice;

        var checkoutData = {
            name: document.getElementById('nameCustomer').value,
            jumlahBayar: jumlahBayar,
            total_price: totalPrice,
            total_quantity: totalQuantity,
            status: status,
            kembalian: kembalian
        };

        fetch('/checkoutdata', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': '{{ csrf_token() }}'
                },
                body: JSON.stringify(checkoutData)
            })
            .then(response => {
                if (!response.ok) {
                    throw new Error('Network response was not ok');
                }
                return response.json();
            })
            .then(data => {
                console.log('Data sent successfully:', data);
                alert('Checkout berhasil');

                localStorage.clear();
            })
            .catch(error => {
                console.error('gagal Checkout:', error);
            });
    }




    function increaseQuantity(index) {
        cartItems[index].quantity++;
        cartItems[index].total = cartItems[index].quantity * cartItems[index].price;
        updateCartUI();
    }

    function decreaseQuantity(index) {
        if (cartItems[index].quantity > 1) {
            cartItems[index].quantity--;
            cartItems[index].total = cartItems[index].quantity * cartItems[index].price;
            updateCartUI();
        } else {

            cartItems.splice(index, 1);
            updateCartUI();
        }
    }


    // Fungsi untuk menghapus item dari keranjang
    function removeItem(id) {
        document.getElementById("confirmation-overlay").addEventListener("click", function(event) {
            if (event.target.classList.contains("yes-button")) {
                var id = event.target.getAttribute("data-id");
                removeItem(id);
            } else if (event.target.classList.contains("no-button")) {
                document.getElementById("confirmation-overlay").style.display = "none";
            }
        });

        var itemIndex = cartItems.findIndex(item => item.id === id);



        if (itemIndex !== -1) {

            cartItems.splice(itemIndex, 1);
            updateCartUI();
            saveCartToStorage();
            document.getElementById("confirmation-overlay").style.display = "none";
            document.getElementById("successPopup").style.display = "flex";
            setTimeout(function() {
                document.getElementById("successPopup").style.display = "none";
            }, 2000);
        } else {
            console.error("Item with ID " + id + " not found in cartItems array.");
        }
    }






    // fungsi search
    function performSearch() {
        var searchInput = document.getElementById('search-input').value.toLowerCase();
        var products = document.querySelectorAll('.product-item');
        var found = false;

        products.forEach(function(product) {
            var productName = product.querySelector('h3').textContent.toLowerCase();
            if (productName.includes(searchInput)) {
                product.style.display = 'block';
                found = true;
            } else {
                product.style.display = 'none';
            }
        });

        if (searchInput === '') {
            products.forEach(function(product) {
                product.style.display = 'block';
            });
        }

        var noResultMessage = document.getElementById('no-result-message');
        if (!found && searchInput !== '') {
            if (!noResultMessage) {
                noResultMessage = document.createElement('p');
                noResultMessage.textContent = 'Hasil tidak ditemukan';
                noResultMessage.id = 'no-result-message';
                noResultMessage.classList.add('text-white', 'bg-danger', 'fw-bold', 'text-center', 'rounded');
                document.getElementById('search-results').appendChild(noResultMessage);
            }
        } else {
            if (noResultMessage) {
                noResultMessage.remove();
            }
        }
    }

    document.getElementById('search-input').addEventListener('input', performSearch);

    document.getElementById('search-form').addEventListener('submit', function(event) {
        event.preventDefault();
        performSearch();
    });
</script>
